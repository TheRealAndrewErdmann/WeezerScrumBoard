/**
 * Enum representing different categories for software changes or tasks.
 * @author yingdongfeng
 */
public enum Category {
    /** Represents a software defect or malfunction. */
    Bug,
    
    /** Represents a new feature or functionality to be added. */
    Feature,
    
    /** Represents tasks related to software documentation. */
    Documentation,
    
    /** Represents code restructuring without changing its external behavior. */
    Refactoring;
}

