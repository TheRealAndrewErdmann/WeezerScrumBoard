/**
 * Enum representing different roles in a Scrum team.
 * @author yingdongfeng
 */
public enum Role {
    /** Represents the product owner role, responsible for defining product features. */
    ProductOwner, 
    
    /** Represents the scrum master role, responsible for ensuring the team follows Scrum practices. */
    ScrumMaster, 
    
    /** Represents the developer role, responsible for implementing features and fixing bugs. */
    Developer;
}
